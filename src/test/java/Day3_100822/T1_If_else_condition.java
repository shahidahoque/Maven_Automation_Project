package Day3_100822;

public class T1_If_else_condition {
    public static void main(String[] args){
        //declared int variables for if else condition
        int a, b;
        a = 2;
        b = 3;

        //verify if a is less than b or not
        if(a < b){
            System.out.println("a is less than b");
        } else{
            System.out.println("a is either greater than or equal to b");
        } //end of conditions

    } // end of main
} // end of class
